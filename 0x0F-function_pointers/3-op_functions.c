#include "3-calc.h"


/**
 * op_add - add two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of operation
*/

int op_add(int a, int b)
{
return (a + b);
}

/**
 * op_sub - subtract two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of operation
*/
int op_sub(int a, int b)
{
return (a - b);
}

/**
 * op_mul - multiply two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of operation
*/
int op_mul(int a, int b)
{
return (a * b);
}

/**
 * op_div - divide two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of operation
*/
int op_div(int a, int b)
{
return (a / b);
}

/**
 * op_mod - modulu two integers
 * @a: first integer
 * @b: second integer
 * Return: the result of operation
*/

int op_mod(int a, int b)
{
return (a % b);
}
